                                    <p>Духовное пробуждение Европы, которое началось в конце XII века, было следствием подъема средневековой городской культуры и выразилось в новых формах деятельности — интеллектуальной и культурной. В частности — расцвет схоластической науки, пробуждение интереса к античности, проявление самосознания личности в религиозной и светской сфере, в искусстве — готический стиль.

                                        Этот процесс духовного пробуждения шел двумя путями (из-за социально-экономических, национальных и культурных особенностей):</p>
                                    <ul class="north_sp">
                                        <li>- развитие элементов светского гуманистического мировоззрения;</li>
                                        <li>- развитие идей религиозного «обновления»;</li>
                                    </ul>

                                    <p>Оба эти течения нередко соприкасались и сливались, но по сути все же выступали как антагонисты. По первому пути пошла <i>Италия</i>, по второму — <i>Северная Европа</i>, пока еще — с формами зрелой готики, с её общим спиритуалистическим настроем и натурализмом деталей.

                                        Итальянский Ренессанс практически не имел влияния на другие страны до 1450 г. После 1500 г. стиль распространился по континенту, но многие позднеготические влияния сохранялись даже до наступления эпохи <a href="#">барокко</a>.

                                    <p><b><i>Основные отличия:</i></b></p>
                                    <ul class="north_sp">
                                        <li>- большее влияние готического искусства;</li>
                                        <li>- меньшее внимание к изучению анатомии и античного наследия;</li>
                                        <li>- тщательная и детализированная техника письма;</li>
                                        <li>- важной идеологической составляющей явилась Реформация.</li>
                                    </ul
                                    </p>

