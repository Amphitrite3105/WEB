                                    <h3>Северное возрождение</h3>
                                    <p><i>Северное Возрождение</i> — термин, использующийся для описания эпохи Возрождения в германоязычных странах, или более обобщающе — во всей Европе за пределами Италии, к северу от Альп. Северное Возрождение тесно связано с итальянским Возрождением, но имеется ряд характерных отличий.

                                        Как таковое, Северное Возрождение не являлось однородным: в каждой стране оно имело определённые специфические черты. Так, культурология и искусствоведение разделяют:
                                    <ul class="north_sp">
                                        <li>- немецкое,</li>
                                        <li>- немецкое,</li>
                                        <li>- нидерландское,</li>
                                        <li>- французское,</li>
                                        <li> - испанское,</li>
                                        <li> - английское,</li>
                                        <li>- польское Возрождение и другие.</li>
                                    </ul>
                                    </p>

                                    <figure class="sign text_img img_right">
