    <p>В концертной жизни начала эпохи Ренессанса основным инструментом был <i>орган</i>, но с течением времени состав музыкальных инструментов
        значительно расширился, к уже существовавшим струнным и духовым добавились новые разновидности.
        Например, <i>виолы</i> — семейство струнных смычковых. По форме они напоминают инструменты современного скрипичного семейства
        (скрипку, альт, виолончель) и, возможно, являются их предшественниками (сосуществовали в музыкальной практике до середины XVIII столетия).
        Однако же они имеют важное отличие. Виолы обладают системой резонирующих струн; как правило, их столько же, сколько и основных (шесть-семь).
        Колебания резонирующих струн делают звук виолы мягким, бархатистым, но инструмент трудно использовать в оркестре, так как из- за большого
        числа струн он быстро расстраивается.</p>

    <p>В семействе виол выделяются три основных типа:</p>
    <ul>
        <li><strong>1)</strong> виола да гамба ;</li>
        <li><strong>2)</strong> виола да браччож</li>
        <li><strong>3)</strong> виоль д’амур.</li>
    </ul>
    <p><i>Виола да гамба</i> — большой инструмент, который исполнитель ставил вертикально и зажимал с боков ногами
        (итальянское слово gamba означает «колено»).
        Две другие разновидности — <i>виола да браччо</i> (от итал. braccio — «предплечье») и <i>виоль д’амур</i> (фр. viole d’amour — «виола любви»)
        были ориентированы горизонтально, и при игре их прижимали к плечу. Виола да гамба по диапазону звучания близка к виолончели,
        виола да браччо — к скрипке, а виоль дьамур — к альту.</p>

    <p>Среди щипковых инструментов Возрождения центральное место занимает </i>лютня</i> (польск. lutnia, от араб. альуд‎ — «дерево»).
        В Европу она пришла с Ближнего Востока в конце XIV в., а уже к началу XVI столетия для этого инструмента был создан богатый репертуар;
        прежде всего под аккомпанемент лютни исполняли песни. Впрочем, для лютни было создано и большое количество инструментальных произведений.
        У лютни короткий корпус; верхняя часть плоская, а нижняя напоминает полусферу. К широкой шейке приделан гриф, разделённый ладами,
        а головка инструмента отогнута назад почти под прямым углом. При желании можно в облике лютни увидеть сходство с чашей.
        Двенадцать струн группируются парами, а звук извлекают как пальцами, так и специальной пластинкой — плектром.</p>

    <p>В XV—XVI столетиях возникли различные виды клавишных. Основные типы таких инструментов — <i>клавесин</i>, <i>клавикорд</i>,
        <i>чембало</i>, <i>вёрджинел</i> — активно использовались в музыке Возрождения, но их настоящий расцвет наступил позже.</p>
