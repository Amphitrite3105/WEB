    <h3 class="content_caption">Музыкой эпохи Возрождения  - </h3>
    <p>называют период в развитии европейской музыки приблизительно между 1400 и 1600 годами. В Италии начало новой эпохи наступило для
        музыкального искусства в XIV веке. Нидерландская школа сложилась и достигла первых вершин в XV, после чего её развитие все ширилось,
        а влияние так или иначе захватывало и мастеров иных национальных школ. Признаки Возрождения отчётливо проявились во Франции в XVI веке,
        хотя её творческие достижения были велики и бесспорны ещё в предыдущие столетия.</p>

    <p>К XVI веку относится подъём искусства в Германии, Англии и некоторых других странах Западной Европы.
