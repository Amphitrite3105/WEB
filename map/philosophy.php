            <h3 class="content_caption">Философия Возрождения  - </h3>
            <p>направление в европейской философии XV—XVI веков. Характеризуется неприятием официальной католической религиозности и интересом
                к человеческой личности. Внутри течения гуманистов этот интерес выразился в идеях классического
                гуманизма и способствовал утверждению практического критерия истины (опыт + польза), который лёг в основу методологии современных естественных наук.</p>

            <h3>Основные направления</h3>
            <ul>
                <li><strong>1.</strong> <i>Гелиоцентризм</i> - представление о том, что Солнце является центральным небесным телом, вокруг которого обращается Земля и другие планеты;</li>
                <li><strong>2.</strong> <i>Гуманизм</i> - демократическая, этическая жизненная позиция, утверждающая, что люди имеют право и обязанность определять смысл и форму своей жизни;
                    призывает к построению более гуманного общества посредством этики, основанной на человеческих и других естественных ценностях, в духе
                    разума и свободного поиска, за счёт использования человеческих способностей;</li>
                <li><strong>3.</strong> <i>Неоплатонизм</i>
                    -  идеалистическое направление, зародившееся в эпоху поздней Античности (III век) и опирающееся на терминологию Платона.
                    К характерным чертам неоплатонизма относят:
                    <ul>
                        <li>- наличие "запредельного первоначала" (Единое);</li>
                        <li>- учение о космических иерархиях (Триада: Единое - Нус - Мировая душа);</li>
                        <li>- признание нашего мира ("космоса") "нисхождением единого" (эманацией);</li>
                        <li>- недоверие к материи и материальным формам как к "темницам души";</li>
                        <li>- призыв к "восхождению души к своему истоку" посредством метемпсихоза, теургии, экстаза;</li>
                    </ul>
                </li>
                <li><strong>4.</strong> <i>Секуляризм</i> -  концепция, согласно которой правительство и другие источники норм права должны существовать отдельно от любого типа религий.;</li>
            </ul>
            <p>Философское мышление этого периода можно охарактеризовать как <i>антропоцентрическое</i>.

                В эпоху Возрождения индивид приобретает гораздо большую самостоятельность, он все чаще представляет не тот
                или иной союз, а самого себя. Отсюда вырастает новое самосознание человека и его новая общественная позиция: гордость
                и самоутверждение, сознание собственной силы и таланта становятся отличительными качествами человека.</p>
            <p>Индивид эпохи Возрождения
                склонен приписывать все свои заслуги самому себе. Разносторонность — вот идеал возрожденческого человека. Человек становится творцом
                самого себя. В результате человек уже не нуждается в божественной благодати для своего спасения. По мере того как человек осознает себя
                в качестве творца собственной жизни и судьбы, он оказывается и неограниченным господином над природой.</p>

            <p>В период возрождения огромную значимость приобретает искусство, и как результат, возникает <i>культ человека-творца</i>.
                Творческая деятельность приобретает своего рода сакральный (священный) характер.

                С антропоцентризмом связан характерный для Возрождения <i>культ красоты</i>. В эпоху Возрождения, как никогда раньше, возросла
                ценность отдельного человека. Выше всего в эту эпоху ставится своеобразие и уникальность каждого индивида.</p>

            <p>В эпоху Возрождения философия вновь обращается к изучению природы. Интерес к натурфилософии усиливается к концу XV —
                началу XVI века по мере того, как пересматривается средневековое отношение к природе как несамостоятельной сфере.

                В понимании природы, так же как и в трактовке человека, философия Возрождения имеет свою специфику — природа трактуется <a href="#">пантеистически</a>.
                Христианский Бог здесь как бы сливается с природой, а последняя тем самым обожествляется.</p>

            <p>Натурфилософы Возрождения, например, знаменитый немецкий алхимик и астролог <a href="#">Парацельс</a> применял магико-алхимическое понимание природы,
                выраженное стремлением управлять природой с помощью тайных, оккультных сил, характерное именно для XV-XVI веков. Натурфилософы стремились
                устранить идею творения: мировая душа представлялась как жизненная сила, благодаря которой природа обретает
                самостоятельность и не нуждается больше в потустороннем начале.
            </p>

