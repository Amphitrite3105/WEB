            <p><a href="#">Николай Кузанский</a>, настоящее имя — Николай Кребс; прозвание Кузанский получил из-за места рождения — Куза на Мозеле (1401—1464) — теолог (был кардиналом при папе Пии II), философ, крупный учёный, особенно значимы его исследования в области математики, астрономии и географии. Высказывал идею единства Бога и его проявления в природе (см. пантеизм). Предлагал гелиоцентрическую систему мира, вселенную считал бесконечной, считается, что его идеи оказали влияние на Джордано Бруно, Николая Коперника и Галилея. Подчёркивал познавательную мощь человека («человек есть его ум»), уподобляя творческие способности божественным. Одной из центральных тем его философии являются сомнения и противоречия. Он рассуждал о границах
                применимости закона противоречия в математическом познании, и о возможности применения математических понятий в познании природы.</p>
