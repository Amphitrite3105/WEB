            <p><a href="#">Мишель Эйкем де Монтень</a>(1533—1592) — выдающийся философ и писатель эпохи Возрождения, автор книги «Опыты», в которой с литературной красочностью проводится тонкий анализ душевных переживаний человека. Исходный пункт учения — скептицизм. В отличие от агностицизма скептицизм Монтеня не отрицает познаваемости мира. Монтень считал, что человеку необходимо постоянно совершенствовать своё мышление путём познания закономерностей природы. Высказывал идею равенства всех людей, идеализировал «естественное состояние» человечества, которое нарушил прогресс цивилизации. Считал, что счастье людей возможно лишь при отсутствии сословий и социального неравенства. Главный принцип его морали: человек
                не должен пассивно ожидать своего счастья, которое религия обещает ему на небесах, он вправе стремиться
                к счастью в жизни земной.</p>
