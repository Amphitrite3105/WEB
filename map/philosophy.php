            <p><a href="#">Джордано Бруно</a> (1548—1600) — итальянский философ, один из последних представителей философии эпохи Возрождения. Придерживался пантеизма, согласно которому Бог отождествляется с мировым целым. Призывал познавать не сверхприродного Бога, а саму природу, являющуюся «Богом в вещах». Разделял гелиоцентрическую теорию Николая Коперника, высказывал идею диалектического единства противоположностей (на бесконечности прямая смыкается в окружность, периферия совпадает с центром, материя сливается с формой). Единицей сущего считал монаду, в деятельности которой оказываются слиянной телесное и духовное, объект и субъект. «Монадой монад» является Бог. В этических воззрениях придерживался идеи «героического энтузиазма» и безграничной любви к бесконечности, возвышающей людей.
                17 февраля 1600 г. был заживо сожжён как еретик на площади Цветов в Риме после 8-летнего заключения.В своих произведениях Бруно часто обращался к имени Гермеса Трисмегиста. Представление о Бруно как о герметике и «ренессансном маге» содержится в работе Фрэнсис Йейтс «Джордано Бруно и герметическая традиция» (1964).</p>


