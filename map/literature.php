            <h4>Германия</h4>

            <p>В XV—XVI веках Германия испытывала экономический подъем, хотя от передовых стран Европы — Италии, Франции, Нидерландов — она отстает.
                Особенность Германии в том, что развитие на её территории шло неравномерно.
                Разные города находились на разных торговых путях, и торговали с разными партнерами.
                Некоторые города вообще находились в стороне от торговых путей, и сохраняли свой средневековый уровень развития.
                Сильны были и классовые противоречия. Крупная знать укрепляла свою власть за счет императора, а мелкое дворянство разорялось.
                В городах шла борьба между имеющим власть патрициатом и мастерами-ремесленниками.
                Наиболее развитыми были южные города: Страсбург, Аугсбург, Нюрнберг и др., те, что находились ближе к Италии и имели с ней торговые связи.</p>

            <p>Литература Германии тогда была разнородна. Гуманисты писали в основном на латинском языке.
                Это объяснялось культом классической древности и обособленностью гуманистов от жизни и потребностей народа.
                Крупнейшие представители  <i>ученого гуманизма</i> — <a href="#">Иоганн Рейхлин</a> (1455—1522),
                <a href="#">Ульрих фон Гуттен</a> (1488—1523).
                Но кроме этого направления были и другие, была <i>реформаторская литература</i>.
                Её представляют <a href="#">Мартин Лютер</a> (1483—1546) и <a href="#">Томас Мюнцер</a> (1490—1525).
                Лютер, выступавший против римской церкви, поддерживавший сперва народные массы, впоследствии перешел на сторону князей,
                из страха перед крестьянским революционным движением.
                Мюнцер же, напротив, поддерживал крестьянское движение до конца, призывал разрушать монастыри и замки, конфисковывать и
                делить имущество. «Люди голодны, — писал он, — они хотят и должны есть».</p>

            <p>Наряду с латинской литературой ученых гуманистов и агитационно-политической литературой реформаторов развивалась
                и <i>народно-бюргерская</i> литература. Но она ещё сохраняет средневековые черты и несет оттенок провинциализма.
                Представителем и родоначальником одного из направлений бюргерской литературы (сатиры) является <a href="#">Себастьян Брант</a>
                (1457—1521). Другой крупнейший представитель бюргерской литературы — <a href="#">Ганс Сакс</a> (1494—1576), поэт. Его наследие велико - это стихи,
                песни, басни, шванки, фастнахшпили (масленичные фарсы)</p>

            <h4>Нидерланды</h4>
            <p>В Нидерландах Возрождение совпадало с национальным освобождением и реформацией.
                В эпоху Возрождения литература в Нидерландах развивалась прежде всего на латинском языке: знаменитым поэтом был <a href="#">Иоанн Секунд</a>,
                автор «Поцелуев»; а крупнейшим латиноязычным прозаиком и гуманистом — <a href="#">Эразм Роттердамский</a>,
                автор знаменитой «Похвалы глупости», которую он посвятил своему другу <a href="#">Томасу Мору</a>.</p>

            <p>Однако, именно в это время закладывались основы народного литературного языка Нидерландов.
                Величайшим нидерландским поэтом и драматургом был <a href="#">Йост ван ден Вондел</a> (1587—1679),
                автор трагедий на библейские и исторические темы, чьи пропитанные духом времени произведения способствовали дальнейшему
                формированию национального самосознания.</p>

            <p>В период «Золотого века Нидерландов» (XVII век) в Амстердаме формируется «Мёйденский кружок»,
                в который входило много писателей и художников «золотого века», в том числе и его крупнейший деятель <a href="#">Питер Хофт</a>.
                Хофт написал прозаическую «Историю Нидерландов»; так же он писал драмы и любовные стихи, стараясь соединить простоту голландской
                средневековой народной песни с изысканностью веяний Ренессанса. В противоположность Вонделу ненавидел христианскую ортодоксию
                и проповедовал стоицизм и эпикурейство</p>

            <h4>Испания и Португалия</h4>
            <p>Литература в этих странах развивалась своеобразно. Социально-политическая обстановка в них была сложной. В первую очередь, здесь проходила
                Реконкиста, отвоевание земель у мавров. Испания не была единой страной, а состояла из отдельных государств.
                Каждая провинция развивалась поначалу обособленно. Абсолютизм (при Изабелле и Фердинанде) сложился поздно. Во вторую очередь,
                Испания в это время вывозила из колоний огромное количество золота, в ней накапливались огромные богатства, и все это тормозило развитие
                промышленности и формирование буржуазии. Однако, литература испанского и португальского Возрождения богата.</p>
            <p><i>Представители</i>: <a href="#">Мигель Сервантес де Сааведра</a>, оставивший серьёзное наследие, как прозу, так и поэзию; <a href="#">Луис де Камоэнс</a>,
                автор «Лузиад», исторического эпоса португальцев.</p>
            <p>Развивались, как поэзия, так и жанры романа и новеллы. Тогда появился типично испанский жанр <i>плутовского романа</i>.
                Образцы: «Жизнь Ласарильо с Тормеса» (без автора), «Жизнь и похождения Гусмана де Альфараче» (автор — Матео Алеман).</p>

